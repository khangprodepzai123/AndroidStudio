package com.example.myapplication;

public class HoatDong {
    String tieuDe;
    String thoiGian;

    public HoatDong(String tieuDe, String thoiGian) {
        this.tieuDe = tieuDe;
        this.thoiGian = thoiGian;
    }

    public String getTieuDe() { return tieuDe; }
    public String getThoiGian() { return thoiGian; }
}
